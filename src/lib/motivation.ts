// Мотивационные окошки по итогам недели: три исхода × 5 вариантов.
// Выбор детерминированный (по номеру недели) — на одном устройстве текст не «скачет».

export interface WeekFeedbackText { title: string; text: string; button: string; tone: 'great' | 'good' | 'low'; }

const GREAT: Array<[string, string]> = [
  ['Молодец, {name}! 🎉', 'Это была очень удачная неделя — так держать! Такие недели и складываются в большой результат.'],
  ['{name}, вы просто огонь! 🔥', 'Неделя закрыта почти на максимум. Дисциплина — ваша суперсила.'],
  ['Блестящая неделя, {name}! 🏆', 'Всё по плану, без отговорок. Именно так выглядят будущие «до/после».'],
  ['{name}, снимаем шляпу! 👏', 'Неделя выполнена на отлично. Позвольте себе минуту гордости — и продолжаем.'],
  ['Сильно, {name}! 💪', 'Отличная неделя в копилке. Метод работает, когда работаете вы — и вы это доказали.'],
];
const GOOD: Array<[string, string]> = [
  ['Хорошая работа, {name}!', 'Больше половины недели закрыто. Один рывок — и следующая неделя будет отличной.'],
  ['{name}, достойная неделя.', 'Главное сделано. Посмотрите, какие задачи остались, — с них и начнём новую неделю.'],
  ['Неплохо, {name}!', 'Движение есть, и это главное. Маленький совет: самое важное — в начало недели.'],
  ['{name}, вы на верном пути.', 'Неделя засчитана. Стабильность важнее рывков — просто не останавливайтесь.'],
  ['Прогресс есть, {name}!', 'База недели закрыта. Добавьте одну задачу сверху на следующей — и будет «отлично».'],
];
const LOW: Array<[string, string]> = [
  ['{name}, бывает у всех.', 'Неделя прошла тише, чем хотелось. Это не провал — это информация. Новая неделя = чистый лист.'],
  ['Не сдавайтесь, {name}!', 'Слабая неделя не отменяет сильной программы. Начните новую с самой лёгкой задачи — разгонитесь.'],
  ['{name}, спокойно и дальше.', 'Одна тихая неделя ничего не решает. Решает то, что вы снова открыли приложение. Вы здесь — это уже шаг.'],
  ['Перезагрузка, {name}!', 'Прошлую неделю отпускаем. Выберите сегодня ОДНУ задачу и сделайте её — импульс вернётся.'],
  ['{name}, метод прощает.', '12 недель — марафон, а не спринт. Слабые отрезки бывают у всех, кто бежит. Продолжаем.'],
];

export function pickWeekFeedback(name: string | null, week: number, score: number): WeekFeedbackText {
  const pool = score >= 85 ? GREAT : score >= 50 ? GOOD : LOW;
  const tone: WeekFeedbackText['tone'] = score >= 85 ? 'great' : score >= 50 ? 'good' : 'low';
  const [title, text] = pool[(week - 1) % pool.length];
  const n = name?.trim() || 'вы молодец';
  return {
    title: title.replace('{name}', n),
    text: `Неделя ${week} завершена с результатом ${score}%. ${text}`,
    button: tone === 'low' ? 'Продолжаем!' : 'Спасибо! 💪',
    tone,
  };
}
