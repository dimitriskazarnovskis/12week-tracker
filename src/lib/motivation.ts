export function pickWeekFeedback(name: string | null, week: number, score: number): WeekFeedbackText {
  const pool = score >= 85 ? GREAT : score >= 50 ? GOOD : LOW;
  const tips = score >= 85 ? TIPS_GREAT : score >= 50 ? TIPS_GOOD : TIPS_LOW;
  const tone: WeekFeedbackText['tone'] = score >= 85 ? 'great' : score >= 50 ? 'good' : 'low';
  const [title, text] = pool[(week - 1) % pool.length];
  const n = name?.trim() || 'вы молодец';
  return {
    title: title.replace('{name}', n),
    text: `Неделя ${week} завершена с результатом ${score}%. ${text}`,
    tip: tips[(week - 1) % tips.length],
    button: tone === 'low' ? 'Продолжаем!' : 'Спасибо! 💪',
    tone,
  };
}
